/*
 Visualizar la tabla de multiplicar del uno al diez.
 */
package Ejercicios01Unidad03;


public class Ejercicio15 {
    public static void main(String[] args) {
        int i, j;
        for(i= 1; 1<=10; i++){
            for(j= 1; j<=10; j++){
               System.out.println(i + " x " + j + " = " + (i*j)); 
            }
        }
    }
}
