    private void ModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ModificarActionPerformed
        try {
            if (!"".equals(numero_dias.getText())) {
                String Query = "update prestamo set numero_dias = '" + numero_dias.getText() + "' where Codigo_Pelicula = " + codigo_pelicula.getText() + "and numero_socio = " + numero_socio.getText() + ";";
                Statement st = conn.createStatement();
                st.executeUpdate(Query);
            }
            if (!"".equals(fecha_prestamo.getText())) {
                String Query = "update prestamo set fecha_prestamo = '" + fecha_prestamo.getText() + "' where Codigo_Pelicula = " + codigo_pelicula.getText() + "and numero_socio = " + numero_socio.getText() + ";";
                Statement st = conn.createStatement();
                st.executeUpdate(Query);
            }
            if (!"".equals(precio.getText())) {
                String Query = "update prestamo set precio = '" + precio.getText() + "' where Codigo_Pelicula = " + codigo_pelicula.getText() + "and numero_socio = " + numero_socio.getText() + ";";
                Statement st = conn.createStatement();
                st.executeUpdate(Query);
            }

            JOptionPane.showMessageDialog(null, "SE HA MODIFICADO LA TABLA");

        } catch (Exception e) {
            System.out.println(e.getMessage());

        }
    }//GEN-LAST:event_ModificarActionPerformed

    private void ConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ConsultarActionPerformed
        DefaultTableModel tablaDefecto = new DefaultTableModel();
        tablaDefecto.addColumn("NUMERO_SOCIO");
        tablaDefecto.addColumn("CODIGO_PELICULA");
        tablaDefecto.addColumn("PRECIO");
        tablaDefecto.addColumn("NUMERO_DIAS");
        tablaDefecto.addColumn("FECHA_PRESTAMO");

        try {
            Class.forName("com.mysql.jdbc.Driver");
            conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/videoclub?zeroDateTimeBehavior=convertToNull", "root", "123456");
            st = conn.createStatement();

            ResultSet resultado = st.executeQuery("select * FROM prestamo ORDER BY codigo_pelicula");

            while (resultado.next()) {
                Object[] datos = new Object[5];
                for (int i = 0; i < 5; i++) {
                    datos[i] = resultado.getObject(i + 1);
                }
                tablaDefecto.addRow(datos);

            }
            Tabla.setModel(tablaDefecto);

        } catch (SQLException ex) {
            System.out.println(ex);
        } catch (ClassNotFoundException ex) {
            System.out.println(ex);
        }
    }//GEN-LAST:event_ConsultarActionPerformed

